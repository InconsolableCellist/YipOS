def render_glyph(atlas, glyph_rows, col, row):
    """Stamp an 8x16 bitmap glyph into the atlas at grid position (col, row)."""
    x0 = col * CELL_W
    y0 = row * CELL_H
    for y, byte in enumerate(glyph_rows):
        for x in range(8):
            if byte & (0x80 >> x):
                atlas.putpixel((x0 + x, y0 + y), (255, 255, 255, 255))


def main():
    atlas = Image.new("RGBA", (ATLAS_W, ATLAS_H), (0, 0, 0, 0))

    glyphs = build_cp437_font()

    for code in range(256):
        col = code % COLS
        row = code // COLS
        if code in glyphs:
            render_glyph(atlas, glyphs[code], col, row)

    atlas.save(OUTPUT_PATH)
    print(f"Saved atlas: {OUTPUT_PATH}")
    print(f"  Size: {atlas.size[0]}x{atlas.size[1]}")

    # Copy to Unity project
    dest_dir = os.path.dirname(COPY_DEST)
    if os.path.isdir(dest_dir):
        shutil.copy2(OUTPUT_PATH, COPY_DEST)
        print(f"Copied to: {COPY_DEST}")
    else:
        print(f"WARNING: destination dir not found: {dest_dir}")


if __name__ == "__main__":
    main()
